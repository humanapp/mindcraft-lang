function pick<T>(items: readonly T[]): T {
  return items[Math.floor(Math.random() * items.length)]!;
}

export function generateTriplet(): string {
  let first = pick(FIRST_WORDS);
  let middle = pick(MIDDLE_WORDS);
  let last = pick(LAST_WORDS);

  for (let i = 0; i < 12; i++) {
    if (first !== middle && middle !== last && first !== last) break;
    first = pick(FIRST_WORDS);
    middle = pick(MIDDLE_WORDS);
    last = pick(LAST_WORDS);
  }

  return `${first}-${middle}-${last}`;
}
